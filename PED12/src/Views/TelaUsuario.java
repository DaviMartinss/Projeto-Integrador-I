    void ApagarUsuario(){
        
        try{
            
            ControlerUser.ApagarUsuario(Integer.parseInt(txt_id.getText()));
            
        }catch(NumberFormatException e){
            
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
    }
    
    
    
